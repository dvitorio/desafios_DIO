package escola;

public class Turma {
	
	private String sigla;
	private int ano;
	
	
	//Construtor padrão
	public Turma() {
	}
	
	
	//Métodos acessores
	public String getSigla() {
		return sigla;
	}
	public void setSigla(String sigla) {
		this.sigla = sigla;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}

}
