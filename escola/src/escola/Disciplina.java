package escola;

public class Disciplina {
	
	private String nome;
	private int cargaHoraria;
	
	
	//Construtor padrão
	public Disciplina() {
	}
	
	//Métodos acessores
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getCargaHoraria() {
		return cargaHoraria;
	}
	public void setCargaHoraria(int cargaHoraria) {
		this.cargaHoraria = cargaHoraria;
	}
	

}
